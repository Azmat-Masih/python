#This is Escape Sequence Charaters

print("this is my time I am going to be very successfull") #full output in single line 
print("this is my time\nI am going to be very\nsuccessfull") # out put in three lines
print("this is my time\nI am going to be very \"successfull\"") 
print("this is my time\nI am going to be very\t \"successfull\"") 
print("this is my time\nI am going\\ to be very\t \"successfull\"") 