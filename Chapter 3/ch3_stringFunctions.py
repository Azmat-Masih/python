# Types Of String Functions

a = "IamTheFounderAndCeoOfAtlas.ai"
print(len(a)) #This will give us the length of a
print(a.find("m")) #give us the index of m
print(a.replace("a","A")) #replace the small a with capital A
print(a.capitalize()) #Starting alphbet will be capital and other will small
print(a.startswith("s")) #this will give us false because the starting alphbet is  I
print(a.endswith("i")) #true


